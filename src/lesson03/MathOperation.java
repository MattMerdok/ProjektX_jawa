package lesson03;

public class MathOperation {
    public static void main(String[] args) {
        int a = 20;
        int b = 7;

        int c = a + b; // сложение +
        System.out.println("с: " + c);

        // результат вычеслений выражения, находящегося от знака "="
        // будет вычеслен и результат помещён в переменную

        c = a - b; // вычитание -

        c = a * b; // умножение *
        System.out.println("c");
    }
}
